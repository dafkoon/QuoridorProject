package Utilities;

/**
 * Contains constants used in the game.
 */
public abstract class Constants {
    /**
     * The size of each tile on the game board.
     */
    public static final int TILE_SIZE = 50;

    /**
     * The dimension of the game board (number of rows/columns).
     */
    public static final int BOARD_DIMENSION = 9;

    /**
     * The total size of the game board (width/height).
     */
    public static final int BOARD_SIZE = TILE_SIZE * BOARD_DIMENSION;
}

